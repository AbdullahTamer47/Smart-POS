export function parseZatcaQR(qrData: string): {
  sellerName: string;
  vatNumber: string;
  timestamp: string;
  total: number;
  vat: number;
} {
  try {
    const decoded = atob(qrData);
    const parts = decoded.split("\x1D");
    const tags: Record<string, string> = {};

    let i = 0;
    while (i < decoded.length) {
      if (i + 1 >= decoded.length) break;
      const tag = decoded.charCodeAt(i).toString();
      i++;
      if (i >= decoded.length) break;
      const length = decoded.charCodeAt(i);
      i++;
      if (i + length > decoded.length) break;
      const value = decoded.substring(i, i + length);
      tags[tag] = value;
      i += length;
    }

    return {
      sellerName: tags["1"] ?? "",
      vatNumber: tags["2"] ?? "",
      timestamp: tags["3"] ?? "",
      total: parseFloat(tags["4"] ?? "0") || 0,
      vat: parseFloat(tags["5"] ?? "0") || 0,
    };
  } catch {
    return { sellerName: "", vatNumber: "", timestamp: "", total: 0, vat: 0 };
  }
}

export function generateZatcaQR(
  sellerName: string,
  vatNumber: string,
  timestamp: string,
  total: number,
  vat: number
): string {
  const encodeTLV = (tag: string, value: string): string => {
    return tag + String.fromCharCode(value.length) + value;
  };

  const parts = [
    encodeTLV("\x01", sellerName),
    encodeTLV("\x02", vatNumber),
    encodeTLV("\x03", timestamp),
    encodeTLV("\x04", total.toFixed(2)),
    encodeTLV("\x05", vat.toFixed(2)),
  ];

  return btoa(parts.join(""));
}