export function formatCurrency(amount: number, currency?: string, locale?: string): string {
  try {
    return new Intl.NumberFormat(locale ?? "en-US", {
      style: "currency",
      currency: currency ?? "USD",
      minimumFractionDigits: 2,
      maximumFractionDigits: 2,
    }).format(amount);
  } catch {
    const symbol = (currency && CURRENCY_SYMBOLS[currency]) ? CURRENCY_SYMBOLS[currency] ?? "$" : "$";
    return `${symbol}${amount.toFixed(2)}`;
  }
}

export function formatDate(date: Date | string, format?: string, locale?: string): string {
  const d = typeof date === "string" ? new Date(date) : date;
  if (isNaN(d.getTime())) return "Invalid Date";

  if (!format) {
    return new Intl.DateTimeFormat(locale ?? "en-US", {
      year: "numeric",
      month: "2-digit",
      day: "2-digit",
    }).format(d);
  }

  const pad = (n: number) => n.toString().padStart(2, "0");
  const replacements: Record<string, string> = {
    YYYY: d.getFullYear().toString(),
    YY: d.getFullYear().toString().slice(-2),
    MM: pad(d.getMonth() + 1),
    DD: pad(d.getDate()),
    HH: pad(d.getHours()),
    mm: pad(d.getMinutes()),
    ss: pad(d.getSeconds()),
    M: (d.getMonth() + 1).toString(),
    D: d.getDate().toString(),
  };

  let result = format;
  for (const [key, value] of Object.entries(replacements)) {
    result = result.replace(key, value);
  }
  return result;
}

export function formatDateTime(date: Date | string, locale?: string): string {
  const d = typeof date === "string" ? new Date(date) : date;
  if (isNaN(d.getTime())) return "Invalid Date";
  return new Intl.DateTimeFormat(locale ?? "en-US", {
    year: "numeric",
    month: "2-digit",
    day: "2-digit",
    hour: "2-digit",
    minute: "2-digit",
    second: "2-digit",
    hour12: false,
  }).format(d);
}

export function generateInvoiceNumber(type: string): string {
  const now = new Date();
  const year = now.getFullYear().toString();
  const random = Math.floor(Math.random() * 1000000)
    .toString()
    .padStart(6, "0");
  const prefix = type.toUpperCase().replace(/[^A-Z]/g, "").slice(0, 6) || "INV";
  return `${prefix}-${year}-${random}`;
}

export function generateSKU(): string {
  const chars = "ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789";
  const segment = (len: number) => {
    let s = "";
    for (let i = 0; i < len; i++) {
      s += chars[Math.floor(Math.random() * chars.length)] ?? "";
    }
    return s;
  };
  return `${segment(4)}-${segment(3)}-${segment(3)}`;
}

export function generateBarcode(): string {
  const digits: number[] = [];
  for (let i = 0; i < 12; i++) {
    digits.push(Math.floor(Math.random() * 10));
  }
  let sum = 0;
  for (let i = 0; i < 12; i++) {
    sum += (digits[i] ?? 0) * (i % 2 === 0 ? 1 : 3);
  }
  const checkDigit = (10 - (sum % 10)) % 10;
  digits.push(checkDigit);
  return digits.join("");
}

